//1.Create an array of names.
let names = ['Aldo', 'Shosanna', 'Hans', 'Bridget'];

//2.Sort the array in alphabetical order.
names.sort();
console.log(names); // ['Aldo', 'Bridget', 'Hans', 'Shosanna']

//3.Sort the array in reverse alphabetical order.
names.reverse();
console.log(names); // ['Shosanna', 'Hans', 'Bridget', 'Aldo']